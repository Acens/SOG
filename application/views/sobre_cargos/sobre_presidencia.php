<h2>Sobre a Diretoria</h2>	
<p>Capacidade  de  Liderança  é  o  aspecto  mais  cobrado  do  presidente, 
portanto o  mais desenvolvido durante o  exercício  do  cargo.  Como  líder 
da  equipe,  o  presidente  deve  ser  exemplo  de  comprometimento  e 
dedicação. Ter foco em metas e prazos é fundamental para que a missão 
da  empresa  seja  cumprida,  tal  como,  sua  visão  seja  alcançada.  Como 
representante,  o  presidente  deve  saber  lidar  com  pessoas,  com  uma 
grande  capacidade  de  comunicação,  ele  deve  passar  uma  imagem 
positiva  da  empresa,  para  desse  modo  conseguir  parcerias  e 
credibilidade.  Por  fim,  ele  deve  ter  capacidades  de  analisar  situações, 
para tomar as melhores decisões para a empresa.</p>

<hr />

<h2>Principais atividades</h2>
<h3>Semanalmente</h3>
<p>Organizar  Reuniões  de  Diretoria,  para  acompanhar  o 
andamento das outra diretorias; realizar Reuniões Gerais para acompanhar 
o andamento da  equipe  e mantê-la informada; manter o foco da empresa 
em sua missão e visão; representar externamente a empresa.</p>

<h3>Mensalmente</h3>
<p>Prospectar novos parceiros; analisar o mercado externo 
e interno; manter a empresa próxima da universidade.</p>
<h3>Eventualmente</h3>
<p>Representar  a  empresa  na  Federação;  representar  a 
empresa em eventos.</p>

